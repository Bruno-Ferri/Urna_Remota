    private void btn_cadastrar_candidatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrar_candidatoActionPerformed
        UrnaClient uc = new UrnaClient();
        if (txt_cad_candidato_nome.getText().isEmpty() || txt_cad_candidato_legenda.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        } else {
            try {
                uc.cadastrarCandidato(txt_cad_candidato_nome.getText(), Integer.parseInt(txt_cad_candidato_legenda.getText()));
            } catch (MalformedURLException ex) {
                Logger.getLogger(FrameCadastroCandidato.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        dispose();
    }//GEN-LAST:event_btn_cadastrar_candidatoActionPerformed

    private void txt_cad_candidato_legendaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cad_candidato_legendaKeyTyped
        String caracteres = "0987654321";
        if (!caracteres.contains(evt.getKeyChar() + "")) {
            evt.consume();
        }
        if ((txt_cad_candidato_legenda.getText().length() > 5)) {
            evt.consume();
            txt_cad_candidato_legenda.setText(txt_cad_candidato_legenda.getText().substring(0, 5));
        }
    }//GEN-LAST:event_txt_cad_candidato_legendaKeyTyped

