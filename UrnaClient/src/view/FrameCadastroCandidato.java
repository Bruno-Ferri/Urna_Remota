    private void btn_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_atualizarActionPerformed
        UrnaClient uc = new UrnaClient();
        if (txt_cad_candidato_nome.getText().isEmpty() || txt_cad_candidato_legenda.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        } else {
            try {
                uc.atualizarCandidato(txt_cad_candidato_nome.getText(), Integer.parseInt(txt_cad_candidato_legenda.getText()));
            } catch (MalformedURLException ex) {
                Logger.getLogger(FrameCadastroCandidato.class.getName()).log(Level.SEVERE, null, ex);
            }
            dispose();
        }
    }//GEN-LAST:event_btn_atualizarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int resp = JOptionPane.showConfirmDialog(null,
                "Tem certeza que deseja excluir " + txt_cad_candidato_nome.getText() + " do registro?");
        UrnaClient uc = new UrnaClient();
        if (resp == 0) {
            try {
                uc.apagarCandidato(Integer.parseInt(txt_cad_candidato_legenda.getText()));
            } catch (MalformedURLException ex) {
                Logger.getLogger(FrameCadastroCandidato.class.getName()).log(Level.SEVERE, null, ex);
            }
            dispose();
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

    private void txt_cad_candidato_legendaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_cad_candidato_legendaFocusLost
        if(!"".equals(txt_cad_candidato_legenda.getText())){
        UrnaClient uc = new UrnaClient();
            try {
                if (uc.verificarCandidato(Integer.parseInt(txt_cad_candidato_legenda.getText())) == true) {
                    String[] candidato = uc.preencherCadastroCandidato(Integer.parseInt(txt_cad_candidato_legenda.getText()));
                    txt_cad_candidato_nome.setText(candidato[1]);
                    btn_atualizar.setEnabled(true);
                    btn_excluir.setEnabled(true);
                }
            } catch (MalformedURLException ex) {
                Logger.getLogger(FrameCadastroCandidato.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txt_cad_candidato_legendaFocusLost

